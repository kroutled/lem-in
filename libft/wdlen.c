#include "libft.h"

size_t		wdlen(const char *str)
{
	size_t	cnt;

	cnt = 0;
	while (1)
	{
		if (str[cnt] == '\0' || str[cnt] == ' ' || str[cnt] == '\t')
			break ;
		cnt++;
	}
	return (cnt);
}
